    import { Share, Copy } from 'lucide-svelte';
    import {toast} from "svelte-sonner";

    export let sharedLink: string;
    export let reviewFor: string;

    let menuOpen = writable(false);
    let copied = writable(false);

    function toggleMenu() {
        menuOpen.update(n => !n);
    }

    function closeMenu() {
        menuOpen.set(false);
    }

    function shareOnWhatsApp() {
        const text = `Check out this review for *${reviewFor}* on Concordia.courses:\n${sharedLink}`;
        const encodedText = encodeURIComponent(text);
        const whatsappUrl = `https://wa.me/?text=${encodedText}`;
        window.open(whatsappUrl);
    }

    function shareOnReddit() {
        const title = `Check out this review for ${reviewFor} on Concordia.courses`;
        const encodedTitle = encodeURIComponent(title);
        const encodedUrl = encodeURIComponent(sharedLink);
        const redditUrl = `https://www.reddit.com/submit?url=${encodedUrl}&title=${encodedTitle}`;
        window.open(redditUrl);
    }

    function handleCopyLink() {
        navigator.clipboard.writeText(sharedLink).then(() => {
            copied.set(true);
        });
        toast.success('Link copied to clipboard');
    }

    function handleShareAction(actionFn: () => void) {
        return () => {
            actionFn();
            closeMenu();
        };
    }

    function clickOutside(node: HTMLElement) {
        const handleClick = (event) => {
            if (node && !node.contains(event.target) && $menuOpen) {
                closeMenu();
            }
        };

        document.addEventListener('click', handleClick);

        return {
            destroy() {
                document.removeEventListener('click', handleClick);
            }
        };
    }

</script>

